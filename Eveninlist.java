package javainterview;

public class Eveninlist {
	public static void main(String[] args) {
		//int[] array = new int[10];
		int[] a= {4,5,6,8,3,2,0,1};
		for(int i=0;i<a.length;i++) {
			if (a[i]%2==0) {
			System.out.println(a[i]+"even ");}
			else {
			System.out.println(a[i]+"odd");}
		}
		
		
		
	}

}
